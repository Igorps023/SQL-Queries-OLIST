SELECT  
        UPPER(T2.SELLER_CITY),
        UPPER(T3.product_category_name),
        SUM(PRICE) AS TOTAL

FROM TB_ORDER_ITEMS AS T1

LEFT JOIN TB_SELLERS AS T2
ON T1.SELLER_ID = T2.SELLER_ID

LEFT JOIN TB_PRODUCTS AS T3
ON T1.PRODUCT_ID = T3.PRODUCT_ID

GROUP BY SELLER_CITY, product_category_name
HAVING SELLER_CITY LIKE '%LAURO%' 
ORDER BY TOTAL DESC